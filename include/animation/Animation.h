#ifndef _ANIMATION_H_
#define _ANIMATION_H_

// Ukljucivanje korsnicki defininisanih biblioteka.
#include "../animation/KeyFrame.h"

// Ukljucivanje biblioteka
#include <vector>

// Koriscenje prostora imena za jednostavniji i pregledniji kod.
using namespace std;

////////////////////////////////////////////////////////////////////////////////
/// @brief Prostor imena animation.
/// Sadrzi sve klase, funkcije i promenljive za rad sa animacajima modela.
////////////////////////////////////////////////////////////////////////////////
namespace animation {

  //////////////////////////////////////////////////////////////////////////////
  /// @brief Klasa Animation odredjuje animaciju modela.
  /// Pomocu klase Animation cuvamo podatke o animaciji modela.
  //////////////////////////////////////////////////////////////////////////////
  class Animation {

  // Privatne promenljive klase.
  private:
    /// Duzina trajanja animacije u sekundama.
    float length;

    /// Vektor pokazivaca na instance klase KeyFrame.
    vector<KeyFrame *> keyFrames;

  // Javne funkcije klase
  public:
    ////////////////////////////////////////////////////////////////////////////
    /// @brief Konstruktor klase.
    /// @param length Duzina trajanja animacije u sekundama.
    /// @param keyFrames Vektor pokazivaca na instance klase KeyFrame.
    ////////////////////////////////////////////////////////////////////////////
    Animation(float length, vector<KeyFrame *> keyFrames);

    ////////////////////////////////////////////////////////////////////////////
    /// @brief Destruktor klase.
    /// @param void
    ////////////////////////////////////////////////////////////////////////////
    ~Animation();

    ////////////////////////////////////////////////////////////////////////////
    /// @brief Funkcija vraca vrednost privatne promenljive length.
    /// @param void
    /// @return float Duzina trajanja animacije.
    ////////////////////////////////////////////////////////////////////////////
    float getLength();

    ////////////////////////////////////////////////////////////////////////////
    /// @brief Funkcija vraca vrednost privatne promenljive keyFrames.
    /// @param void
    /// @return vector<KeyFrame *> Vektor pokazivaca na instance klase KeyFrame.
    ////////////////////////////////////////////////////////////////////////////
    vector<KeyFrame *> getKeyFrames();
  };

} // animation

#endif
